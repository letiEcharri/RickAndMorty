import UIKit

extension UIColor {
    /// #e8f5de
    @objc
    static var lightGreenRM: UIColor {
        return UIColor(red: 232.0 / 255.0, green: 245.0 / 255.0, blue: 222.0 / 255.0, alpha: 1.0)
    }
    
    /// #039d47
    @objc
    static var darkGreenRM: UIColor {
        return UIColor(red: 3.0 / 255.0, green: 157.0 / 255.0, blue: 71.0 / 255.0, alpha: 1.0)
    }
    
    /// #16b2c3
    @objc
    static var turquoiseRM: UIColor {
        return UIColor(red: 22.0 / 255.0, green: 178.0 / 255.0, blue: 195.0 / 255.0, alpha: 1.0)
    }
}
